    {
        if ($request) {

            $daftar = Produk::where('nama_produk', 'LIKE', '%' . $request->search . '%')
                ->orWhere('satuan', 'LIKE', '%' . $request->search . '%')->paginate(5);
        } else {

            $daftar = Produk::paginate(5);
        }
        return view('user.daftar_harga', compact('daftar', 'request'));
    }

